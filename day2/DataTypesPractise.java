package week1.day2;

public class DataTypesPractise {

	public static void main(String[] args) {
		//Datatypes --->Primitive and non primitive
		//Primitive types-Integer,Float,Character,Boolean(Wrapper Classes)
		//Integer types
		byte b=12;
		short s=200;
		int i=7777;
		long l=8940936973L;
		//Float types
		float f=13.734F;
		double d=156.7865477;
		//Character
		char c='F';
		//prints ascii code if you give integers in char
		char c1=77;
		//Boolean--> True or False
		boolean understood=true;
		
		//Non Primitive--String and Arrays
		//String
		String command="I am ahead of the league";
		
		System.out.println("Integer types: Byte:"+b+"  Short:"+s+" Int:"+i+" Long:"+l);
		System.out.println("Float types: Float:"+f+"  Double:"+d);
		System.out.println("Char:"+c+" Ascii value for char77 is:"+c1);
		System.out.println("Boolean value:"+understood);
		

	}

}
